void main(){

  ///Switch case statement
  String grade = 'A';

  switch (grade){

    case 'A':
      print("Excellent grade of A");
      break;


    case 'B':
      print("Very Good !");
      break;

    case 'C':
      print("Good but work hard");
      break;


    case 'D':
      print('Yes i will print the d statement');
      break;

    default:
      print("you have failed");
  }
}